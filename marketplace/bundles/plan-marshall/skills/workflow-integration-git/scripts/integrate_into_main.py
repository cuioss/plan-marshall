# SPDX-License-Identifier: FSL-1.1-ALv2
"""Atomic finalize move-back: integrate the worktree-resident plan into main.

Notation: ``plan-marshall:workflow-integration-git:integrate_into_main``

This is a single-action standalone script that, in ONE atomic call, performs the
finalize integration step of the move-based, cwd-pinned hermetic worktree model
(ADR-002, solution_outline.md §5). It is the inverse of the D4 move-in script
(``prepare_execute.py``) and runs while the worktree is STILL PRESENT — branch
cleanup removes the worktree AFTER this script returns. In one call it:

  1. **ACQUIRES** the cooperative merge lock (delegating to the unified
     ``plan-marshall:manage-locks:merge_lock`` script — atomic ``O_EXCL`` create,
     holder = this ``plan_id``, simple-backoff retry, plan-liveness stale
     reclamation). The merge lock is the SINGLE deliberate main-anchored
     exception to cwd-relative resolution (ADR-002).
  2. **FOLDS** the plan's OWN global logs (only logs generated for this plan,
     currently resident in the worktree's ``.plan/local/logs/``) into the plan
     directory (``.plan/local/plans/{plan_id}/logs/``) BEFORE move-back so the
     retrospective can read them after the plan dir lands on main.
  3. **MOVES** the plan directory back from the worktree to the main checkout.
  4. **RELEASES** the merge lock — on EVERY exit path, including the rollback
     path, so a crashed-and-retried finalize never wedges the lock.

The on-main executor is NOT regenerated here. Regeneration is a project-level,
meta-project-only finalize step (``finalize-step-sync-plugin-cache``, run after
the cache sync) — only the plan-marshall meta-project's own plans ever modify
``marketplace/bundles/*/skills/*/scripts/``, so only that project ever needs the
on-main executor mapping rebuilt. The executor is a per-tree DERIVED artifact
(ADR-002), never a moved slot and never regenerated by this shipped script.

It RETURNS a status TOON; it does NOT change the caller's cwd (a subprocess
cannot mutate its parent's cwd) and it does NOT remove the worktree (branch
cleanup owns that, after this returns). The finalize orchestrator returns ITS OWN
cwd to main after the call (so the uniform cwd rule resumes main resolution for
retrospective + archive).

SOURCE and DESTINATION are resolved **cwd-independently** so the verb is correct
from ANY cwd (worktree or main): the SOURCE worktree path is resolved via the
canonical ``manage-status get-worktree-path`` channel with a structural
``get_worktree_root() / {plan_id}`` filesystem-probe fallback for the
moved-in-from-main case (the plan dir MOVED off main into its worktree at
phase-5, so the channel's status.json read on main returns NOT_FOUND), mirroring
``git-workflow.py``'s ``cmd_locate_plan_checkout``; the DESTINATION
main plan dir is resolved via the single sanctioned main-anchored resolver
``resolve_main_anchored_path('plans/{plan_id}')`` (ADR-002). Neither resolution
depends on the process cwd, so the idempotence guard returns ``noop`` ONLY when
the SOURCE is genuinely absent AND an AUTHORITATIVE (``status.json``-bearing)
main DESTINATION is present. A ``status.json``-less orphan slot at the
destination (``logs/``/``work/`` residue only) is reclaimed by the move-back,
not treated as a completed integration.

Atomic-with-rollback (solution_outline.md §5): a partial move-back failure rolls
the plan dir BACK into the worktree so the authoritative copy is never split, and
the merge lock is released on the rollback path too. Idempotent: an
already-moved-back plan (real plan dir already resident on main, none in the
worktree) is a no-op success.

See the TOCTOU / check-then-act mitigation menu in
``ref-code-quality/standards/code-organization.md#toctou--check-then-act-hazards``
and the lock-correctness notes in ``manage-locks/scripts/merge_lock.py``.
"""

